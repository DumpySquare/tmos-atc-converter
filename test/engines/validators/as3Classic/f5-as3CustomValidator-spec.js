'use strict';

const cloneDeep = require('lodash/cloneDeep');

const as3Validator = require('../../../../src/validators/as3');
const assert = require('../../../testUtils/assert');

describe('f5-as3/as3CustomValidator', () => {
    describe('.validate', () => {
        const invalidDeclarationInput = {
            AS3_Tenant: {
                class: 'Tenant',
                AS3_Application: {
                    class: 'Application',
                    test_vs: {
                        class: 'Service_TCP',
                        layer4: 'tcp',
                        profileTCP: {
                            bigip: '/Common/tcp'
                        },
                        virtualAddresses: [
                            '2001:db8::5'
                        ],
                        virtualPort: 443,
                        persistenceMethods: [],
                        test: 'test5'
                    },
                    test4: 'test'
                },
                test: 'test',
                test2: 'test2',
                test3: 'test3'
            }
        };
        const invalidDeclarationOutputLazy = {
            AS3_Tenant: {
                class: 'Tenant',
                AS3_Application: {
                    class: 'Application',
                    test_vs: {
                        class: 'Service_TCP',
                        layer4: 'tcp',
                        profileTCP: {
                            bigip: '/Common/tcp'
                        },
                        virtualAddresses: [
                            '2001:db8::5'
                        ],
                        virtualPort: 443,
                        persistenceMethods: []
                    }
                }
            }
        };

        const validDeclaration = {
            class: 'ADC',
            schemaVersion: '3.47.0',
            id: 'urn:uuid:b4ff2177-f0b7-48b1-82ab-ed639379e8fc',
            label: 'F5 BIG-IP Converted Declaration',
            remark: 'Generated by Automation Config Converter',
            updateMode: 'selective',
            Common: {
                class: 'Tenant',
                optimisticLockKey: '',
                Shared: {
                    class: 'Application',
                    enable: true,
                    template: 'shared',
                    test_pool: {
                        allowNATEnabled: true,
                        allowSNATEnabled: true,
                        loadBalancingMode: 'round-robin',
                        members: [
                            {
                                addressDiscovery: 'static',
                                adminState: 'enable',
                                connectionLimit: 0,
                                dynamicRatio: 1,
                                enable: true,
                                priorityGroup: 0,
                                rateLimit: -1,
                                ratio: 1,
                                servicePort: 443,
                                serverAddresses: [
                                    '10.10.10.1',
                                    '10.10.10.2'
                                ],
                                shareNodes: true
                            }
                        ],
                        minimumMembersActive: 1,
                        reselectTries: 0,
                        serviceDownAction: 'none',
                        slowRampTime: 10,
                        class: 'Pool'
                    }
                }
            }
        };

        it('should return right validation result for invalid declaration', async () => {
            const result = await as3Validator.validate(cloneDeep(invalidDeclarationInput));
            assert.deepStrictEqual(result.data, invalidDeclarationOutputLazy);
            assert.isFalse(result.isValid);
            assert.isNotEmpty(result.errors);
            assert.isNotEmpty(result.ignoredAttributes);
            assert.isNotEmpty(result.ignoredAttributesErrors);
        });

        it('should return right result in "strict" mode for invalid declaration', async () => {
            const result = await as3Validator.validate(cloneDeep(invalidDeclarationInput), { mode: 'strict' });
            assert.deepStrictEqual(result.data, invalidDeclarationInput);
            assert.isFalse(result.isValid);
            assert.isNotEmpty(result.errors);
        });

        it('should return right validation result for valid declaration', async () => {
            const expected = {
                data: cloneDeep(validDeclaration),
                errors: [],
                ignoredAttributes: [],
                ignoredAttributesErrors: [],
                isValid: true
            };
            const result = await as3Validator.validate(cloneDeep(validDeclaration));
            assert.deepStrictEqual(result, expected);
        });

        it('should return right result in "strict" mode for invalid declaration', async () => {
            const expected = {
                data: cloneDeep(validDeclaration),
                errors: [],
                isValid: true
            };
            const result = await as3Validator.validate(cloneDeep(validDeclaration), { mode: 'strict' });
            assert.deepStrictEqual(result, expected);
        });
    });
});
